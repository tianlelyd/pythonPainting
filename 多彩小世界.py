def clouds():
    speed(0)
    for m in range(10):
        x, y = randint(-200, 350), randint(100, 230)
        up()
        goto(x, y)
        down()
        onecloud(uniform(0,0.1),uniform(0.6,1),1)
    up()
    goto(0,0)
    down()
    seth(0)

def onecloud(r,g,b):
    hideturtle()
    speed(0)
    pensize(8)
    pencolor(uniform(0,0.1),uniform(0.6,1),1)
    fillcolor((r,g,b))
    begin_fill()
    seth(7)
    for i in range(randint(18,23)):
        fd(i)
        left(1)
    seth(90)
    for n in range(3):
        circle(30,200)
        right(60)
    end_fill()

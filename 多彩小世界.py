def tree(length,level):
    speed(0)
    pensize(8)
    pencolor(choice(lstree))
    if level<0:
        return
    fd(length)
    left(45)
    tree(0.6*length,level-1)
    right(90)
    tree(0.6*length,level-1)
    left(45)
    backward(length)
    return
